import 'package:flutter/widgets.dart';

class StudentExpansionWidget extends StatefulWidget {
  final Widget? child;
  final bool expand;

  StudentExpansionWidget({this.expand = false, this.child});

  @override
  State<StatefulWidget> createState() => StudentExpansionWidgetState();
}

class StudentExpansionWidgetState extends State<StudentExpansionWidget> with SingleTickerProviderStateMixin {
  late AnimationController expandController;
  late Animation<double> animation;

  @override
  void initState() {
    super.initState();
    prepareAnimations();
    _runExpandCheck();
  }

  @override
  Widget build(BuildContext context) {
    return SizeTransition(
      axisAlignment: 1.0,
      sizeFactor: animation,
      child: widget.child,
    );
  }

  void prepareAnimations() {
    expandController = AnimationController(
      vsync: this,
      duration: Duration(milliseconds: 300),
    );
    animation = CurvedAnimation(parent: expandController, curve: Curves.ease);
  }

  void _runExpandCheck() {
    if (widget.expand) {
      expandController.forward();
    } else {
      expandController.reverse();
    }
  }

  @override
  void didUpdateWidget(StudentExpansionWidget oldWidget) {
    super.didUpdateWidget(oldWidget);
    _runExpandCheck();
  }

  @override
  void dispose() {
    expandController.dispose();
    super.dispose();
  }
}
