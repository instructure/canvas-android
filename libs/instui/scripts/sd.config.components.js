function capitalize(str) {
  return str.charAt(0).toUpperCase() + str.slice(1)
}

function toCamelCase(str) {
  return str
    .replace(/[-_](.)/g, (_, c) => c.toUpperCase())
    .replace(/^(.)/, c => c.toLowerCase())
}

